function TIC()
	cls(4)
	for x=0,240 do
		for y=0,180 do
		 rx = x / 240 - .5
			ry = y / 180 - .5
			if math.sin(rx * 10 + time()*.003)*.2 > ry + .1 then
			 pix(x,y,15)
			end
		end
	end
end

